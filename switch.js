let price = 20;
switch  (price) {
    case    price < 20:
        console.log("price is too low");
        break;
    case    price > 20:
        console.log("price is too big");
        break;
    case    price = 20:
        console.log("perfect price!!");
        break;
    default:
        console.log("This is not a valid price");
}